package frc.robot.commands;

import edu.wpi.first.wpilibj2.command.CommandBase;

import frc.robot.subsystems.runtimeState.BotStateSubsystem;
import frc.robot.subsystems.ShooterSubsystem;
import frc.robot.subsystems.ShooterSubsystem.BallIndexerMode;
import frc.robot.subsystems.ShooterSubsystem.FlywheelSpeed;

public class ManualMode extends CommandBase {
  BotStateSubsystem m_botState;
  ShooterSubsystem m_shooterSubsystem;
  Boolean HasRan = false;

  /** Creates a new ManualModeToggle. */
  public ManualMode(BotStateSubsystem botState, ShooterSubsystem shooterSubsystem) {
    m_botState = botState;
    m_shooterSubsystem = shooterSubsystem;
    addRequirements(shooterSubsystem, m_botState);
  }

  // Called when the command is initially scheduled.
  @Override
  public void initialize() {
  }

  // Called every time the scheduler runs while the command is scheduled.
  @Override
  public void execute() {
    m_botState.ManualControl = true;
  }

  // Called once the command ends or is interrupted.
  @Override
  public void end(boolean interrupted) {
    m_botState.ManualControl = false;
    m_shooterSubsystem.runFlywheel(FlywheelSpeed.Stopped);
    m_shooterSubsystem.runBallIndexer(BallIndexerMode.Stopped);
    // RobotContainer.m_Climber.runClimberElevator(ClimberMode.Stopped);
    // RobotContainer.m_tRex.setArmPosition(TrexArmPosition.ESTOP);
  }

  // Returns true when the command should end.
  @Override
  public boolean isFinished() {
    return false;
  }
}
